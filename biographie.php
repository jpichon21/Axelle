                <div class="en">
                <p>The year 2019 marks a turning point for Axelle Fanyo since she made her debut in the United States of America. 
                She was one of the ten singers to participate in the SongStudio at Carnegie Hall hosted by Renée Fleming, and she won the Kaleidoscope Competition in Los Angeles.
                She will be back next season in the US for two concerts, one with the Kaleidoscope Chamber Orchestra in october 2019 and a recital at the Phillips Collection of Washington in november 2019.</p>

                <p>After studying musicology at the Sorbonne and winning awards as a violinist at the Conservatoire La Courneuve, Axelle decided to focus on life as a singer.  She studied at the Conservatoire National Supérieur de Musique de Paris (CNSMDP) with Glenn Chambers and obtained her Masters Diploma.
                In the course of her studies, Axelle had the chance to work with the likes of Anne Le Bosec, Susan Manoff, Emmanuelle Cordoliani and Vincent Vittoz</p>

                <p>As a prize-winner at the Fondation Royaumont, she also had the opportunity to study with singers such as Mireille Delunsch, Waltraud Meier and Dame Felicity Lott.
                </p>

                <p>Alongside her operatic studies and performances, she is passionate about song, and regularly appears in recital.   She won first prize at the International Song Competition in Enschede in 2015, and took the Young Artist prize at the International French song competition in Toulouse the same year.  2018 saw her reach the last eight of the Montreal International Competition for song/mélodies.  With pianist Louise Akili, she has performed at several festivals, including the Festival Chopin de Nohant, at the Petit Palais (with Jeunes Talents) and at the Collège des Bernadins as part of their concert series.   This season, she will once again feature in the Jeunes Talents concert series at the Musée d’Orsay.</p>

                <p>In opera, Axelle made her debut at Tourcoing with Jean-Claude Malgoire as Elisabeth in Tannhaüser and Eleanora in Prima la musica et poi la parole by Salieri for Opera Fuoco, with whom she has also appeared as soprano soloist in The Fairy Queen at the Baroque Festival in Shanghai.
                She has also sung Leonora in La Forza del Destino by Verdi at Bazoches Festival, Vitellia in La Clemenza di Tito by Mozart in Dijon Opera House, and Barena in Jenufa by Janáček in Dijon Opera House and Caen Opera House.
                </p>

                <p>At the end of 2018/2019 season, she sang the soprano solo in Ein deutches Requiem by Brahms with Ensemble Aedes and Les Siècles orchestra directed by Mathieu Romano at Théâtre Imperial de Compiègne. This concert will be on tour in several  French Festivals in august.
                </p>

                <p>Next season she will sing Junon in La Divisone del mondo by Giovanni Legrenzi with Christophe Rousset and Les Talens Lyrique at the Kölner Philharmonie, Leïla in I was looking at the ceiling and then I saw the sky by John Adams with the Studio of Lyon Opera House, and in La Carmélite by Reynaldo Hahn with Palazetto Bru Zane and Capitole orchestra.
